async def get_support_response(user_text: str = None, image_bytes: bytes = None, client_data: str = None, current_bank_name: str = None, chat_history: list = None) -> str:
    """Отримання відповіді від моделі OpenRouter (Gemini)"""
    if not client:
        return "Дякуємо за звернення. Адміністратор відповість вам найближчим часом."

    messages = [
        {"role": "system", "content": SYSTEM_INSTRUCTION}
    ]

    # Додаємо контекст клієнта, якщо він є
    context_parts = []
    if client_data:
        context_parts.append(f"Анкетні дані клієнта:\n{client_data}")
    if current_bank_name:
        context_parts.append(f"Поточний банк, який проходить клієнт: {current_bank_name}")
        
    if context_parts:
        messages.append({
            "role": "system",
            "content": "КОНТЕКСТ ПОТОЧНОГО КЛІЄНТА:\n" + "\n\n".join(context_parts)
        })

    # Додаємо історію чату, якщо вона передана
    if chat_history:
        messages.extend(chat_history)


    content = []
    if user_text:
        content.append({"type": "text", "text": user_text})

    if image_bytes:
        base64_image = base64.b64encode(image_bytes).decode('utf-8')
        content.append({
            "type": "image_url",
            "image_url": {
                "url": f"data:image/jpeg;base64,{base64_image}"
            }
        })

    if not content:
        return "Будь ласка, напишіть ваше запитання або надішліть скріншот помилки."

    messages.append({
        "role": "user",
        "content": content
    })

    try:
        response = await client.chat.completions.create(
            model=OPENROUTER_MODEL,
            messages=messages,
            extra_headers={
                "HTTP-Referer": "https://github.com/shaaaaka/telegram-automation-bot",
                "X-Title": "Verification Support Bot"
            }
        )
        raw_response = response.choices[0].message.content.strip()
        
        # Додаємо обов'язковий підпис до кожного повідомлення
        signature = "\n\nЯ всього автоматизатор, якщо я не вирішую вашу проблему зверніться до менеджера"
        return f"{raw_response}{signature}"
    except Exception as e:
        logger.error(f"Помилка при запиті до OpenRouter: {e}")
        return "Виникла помилка при обробці запиту ШІ. Будь ласка, зачекайте на відповідь адміністратора."
