import base64
import logging
from openai import AsyncOpenAI
from bot.config import OPENROUTER_API_KEY, OPENROUTER_MODEL

logger = logging.getLogger(__name__)

# Ініціалізуємо AsyncOpenAI клієнт для OpenRouter
client = None
if OPENROUTER_API_KEY:
    client = AsyncOpenAI(
        api_key=OPENROUTER_API_KEY,
        base_url="https://openrouter.ai/api/v1"
    )
else:
    logger.warning("OPENROUTER_API_KEY не знайдено в конфігурації. ШІ-підтримка буде неактивною.")

SYSTEM_INSTRUCTION = """Ти — ввічливий, спокійний та професійний ШІ-асистент підтримки у Telegram-боті верифікації.
Твоя місія — допомагати клієнтам долати технічні труднощі під час реєстрації в банківських додатках.

ПРОЦЕС РЕЄСТРАЦІЇ КЛІЄНТА:
1. Клієнт запускає бот (/start) і вводить анкету (ПІБ + дата народження, потім свій ІПН).
2. Очікує, поки адмін підбере лінію (номер телефону) та призначить її.
3. Бот надсилає клієнту номер телефону та інструкцію, який саме банківський додаток завантажити.
4. Клієнт вводить цей номер у додатку банку та натискає "Запросити SMS-код".
5. SMS-код автоматично (чи через адміна) пересилається клієнту в бот. Клієнт вводить його в додатку, за потреби проходить верифікацію особи (через Дію чи фото паспорта) та успішно відкриває картку.

ЯК ТИ МАЄШ ВІДПОВІДАТИ НА ЗАПИТАННЯ ТА ТЕХНІЧНІ ПОМИЛКИ:

1. ДІЯ ЗАВИСЛА АБО ДОДАТОК НЕ ЙДЕ ДАЛІ:
   - Якщо клієнт скаржиться, що додаток "завис", "крутиться завантаження", або "не пускає далі після Дії":
     * Порекомендуй повністю закрити додаток банку (вивантажити його з активних додатків у фоні).
     * Зачекати 10-15 секунд.
     * Запустити додаток знову та повторити спробу.
     * Якщо не допомагає, порекомендуй перезавантажити телефон або перевірити стабільність інтернет-зв'язку (вимкнути/увімкнути Wi-Fi чи мобільний інтернет).
     * Попроси зачекати кілька хвилин, оскільки державні сервери Дії або сервери банку можуть бути тимчасово перевантажені.

2. ПРОБЛЕМИ З ОТРИМАННЯМ SMS-КОДУ:
   - Якщо клієнт каже, що код не приходить:
     * Нагадай, що відправка коду може тривати 1-2 хвилини на стороні мобільного оператора.
     * Попроси перевірити, чи правильно введено номер телефону (без зайвих цифр, суворо за шаблоном, який надіслав бот).
     * Якщо час минув, порекомендуй натиснути кнопку "Запросити SMS-код" ще раз у боті.
     * Заспокой клієнта, що адміністратор бачить його статус та вирішує проблему.

3. ПОМИЛКА "ПЕРЕВИЩЕНО ЛІМІТ СПРОБ" АБО "ПОВТОРІТЬ ЧЕРЕЗ X ХВИЛИН":
   - Попроси клієнта зробити саме те, що пише додаток (зачекати вказаний час).
   - Скажи, що адмін знає про цю ситуацію і за потреби замінить номер або допоможе пізніше.

4. ПРОБЛЕМИ З ВВЕДЕННЯМ ІПН ЧИ ВАЛІДАЦІЄЮ:
   - Нагадай, що ІПН має складатися строго з 10 цифр без літер, пробілів та розділових знаків.
   - Запитуємо ІПН виключно для офіційного підтвердження особи в реєстрах банку.

5. НЕ ВДАЄТЬСЯ ЗАВАНТАЖИТИ ДОДАТОК:
   - Якщо посилання не працює, порекомендуй знайти додаток вручну у App Store (iOS) або Google Play (Android) за офіційною назвою (наприклад: "izi bank", "EcoBank", "amobank", "Bank Lviv" тощо).

ПРАВИЛА СПІЛКУВАННЯ:
- Відповідай виключно українською мовою.
- Тон має бути підтримуючим, заспокійливим та ввічливим.
- Пиши лаконічно (1-3 речення), не лій воду. Клієнту потрібна швидка інструкція.
- Ніколи не розкривай внутрішні технічні деталі системи, імена адмінів, паролі чи базу даних.
- Якщо питання занадто складне або потребує втручання людини, скажи: «Я передав інформацію адміністратору. Будь ласка, очікуйте, він зв'яжеться з вами найближчим часом».
"""

async def get_support_response(user_text: str = None, image_bytes: bytes = None) -> str:
    """Отримання відповіді від моделі OpenRouter (Gemini)"""
    if not client:
        return "Дякуємо за звернення. Адміністратор відповість вам найближчим часом."

    messages = [
        {"role": "system", "content": SYSTEM_INSTRUCTION}
    ]

    content = []
    if user_text:
        content.append({"type": "text", "text": user_text})

    if image_bytes:
        base64_image = base64.b64encode(image_bytes).decode('utf-8')
        content.append({
            "type": "image_url",
            "image_url": {
                "url": f"data:image/jpeg;base64,{base64_image}"
            }
        })

    if not content:
        return "Будь ласка, напишіть ваше запитання або надішліть скріншот помилки."

    messages.append({
        "role": "user",
        "content": content
    })

    try:
        response = await client.chat.completions.create(
            model=OPENROUTER_MODEL,
            messages=messages,
            extra_headers={
                "HTTP-Referer": "https://github.com/shaaaaka/telegram-automation-bot",
                "X-Title": "Verification Support Bot"
            }
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        logger.error(f"Помилка при запиті до OpenRouter: {e}")
        return "Виникла помилка при обробці запиту ШІ. Будь ласка, зачекайте на відповідь адміністратора."
