import base64
import logging
from openai import AsyncOpenAI
from bot.config import OPENROUTER_API_KEY, OPENROUTER_MODEL

logger = logging.getLogger(__name__)

# Ініціалізуємо AsyncOpenAI клієнт для OpenRouter
client = None
if OPENROUTER_API_KEY:
    client = AsyncOpenAI(
        api_key=OPENROUTER_API_KEY,
        base_url="https://openrouter.ai/api/v1"
    )
else:
    logger.warning("OPENROUTER_API_KEY не знайдено в конфігурації. ШІ-підтримка буде неактивною.")

SYSTEM_INSTRUCTION = """Ти — ввічливий, спокійний та професійний ШІ-асистент підтримки у Telegram-боті верифікації.
Твоє завдання — допомагати клієнтам долати технічні труднощі під час реєстрації в банківських додатках.

ПРАВИЛА СПІЛКУВАННЯ ТА СТИЛЮ:
- Звертайся до клієнта виключно на "Ви" (з великої літери).
- Відповідай коротко і по ділу, не розписуй дуже багато. Клієнту потрібна швидка та чітка відповідь.
- Відповідай виключно українською мовою.
- Ніколи не вигадуй системних команд та не розкривай внутрішні технічні деталі системи.

ВІДПОВІДІ НА ПОШИРЕНІ ЗАПИТАННЯ ТА ПОМИЛКИ:

1. ЯКУ ПОШТУ СТАВИТИ:
   - Пошту людина ЗАВЖДИ ставить свою власну.

2. ЯКІ АНКЕТНІ ДАНІ СТАВИТИ (якщо запитують про адресу, роботу тощо):
   - Відповідай строго: "Реєструйте як наче під себе".

3. ЯКИЙ ПІН-КОД ЧИ ПАРОЛЬ СТАВИТИ, ТА ІНСТРУКЦІЇ:
   - Пін чи пароль клієнт ставить свій власний (якийсь свій), а потім просто повідомляє/каже його нам (менеджеру) сюди в чат.
   - Також повідом, що менеджер може скинути інструкцію на реєстрацію деяких банків.

4. ЯКЕ КОДОВЕ СЛОВО СТАВИТИ:
   - Клієнт має ставити своє власне ПРІЗВИЩЕ (фамілію).
   - Тобі буде надано контекст сесії клієнта (ПІБ). Проаналізуй ПІБ клієнта, знайди його ПРІЗВИЩЕ та вкажи його у відповіді.
   - Наприклад, якщо у ПІБ написано "Григорович Денис Валерійович", то перше слово "Григорович" — це його прізвище. Тоді відповідай: "Використовуйте Ваше прізвище — Григорович". Завжди витягуй перше слово з ПІБ як прізвище і пропонуй його клієнту.

5. ДІЯ ЗАВИСЛА АБО ДОДАТОК НЕ ЙДЕ ДАЛІ:
   - Порекомендуй повністю закрити додаток банку (вивантажити його з фону), почекати 10-15 секунд та запустити знову. За потреби — перезавантажити телефон.

6. НЕ ПРИХОДИТЬ SMS-КОД:
   - Попроси зачекати 1-2 хвилини (можливі затримки оператора). Перевірити, чи правильно введено номер телефону. За потреби натиснути кнопку "Запросити SMS-код" повторно.

7. УСПІШНА РЕЄСТРАЦІЯ АБО ВИДАЛЕННЯ БАНКУ / АКАУНТУ:
   - Якщо надісланий скріншот або текст підтверджують успішно завершену реєстрацію в банківському додатку (наприклад, успішний вхід, відкритий рахунок з балансом 0.00 грн, екран успіху реєстрації) або успішне видалення акаунту/банку, твоя відповідь ОБОВ'ЯЗКОВО повинна починатися з тегу [SUCCESS_VERIFICATION].
   - Після цього тегу напиши коротке привітання клієнта з успішним завершенням (наприклад: "[SUCCESS_VERIFICATION] Чудово! Реєстрація успішно завершена.").
   - ДОДАТКОВО: Якщо це додаток bank.kd (або на скріншоті видно банківську карту з маскованим номером типу 4132 **** **** 2751), обов'язково знайди перші 4 та останні 4 цифри номера карти на зображенні та додай до тексту своєї відповіді спеціальний маркер у форматі: [CARD_MASK: XXXX...YYYY] (наприклад: [CARD_MASK: 4132...2751]). Якщо номер карти не видно або неможливо розпізнати, не додавай цей маркер.
"""

async def get_support_response(user_text: str = None, image_bytes: bytes = None, client_data: str = None, current_bank_name: str = None) -> str:
    """Отримання відповіді від моделі OpenRouter (Gemini)"""
    if not client:
        return "Дякуємо за звернення. Адміністратор відповість вам найближчим часом."

    messages = [
        {"role": "system", "content": SYSTEM_INSTRUCTION}
    ]

    # Додаємо контекст клієнта, якщо він є
    context_parts = []
    if client_data:
        context_parts.append(f"Анкетні дані клієнта:\n{client_data}")
    if current_bank_name:
        context_parts.append(f"Поточний банк, який проходить клієнт: {current_bank_name}")
        
    if context_parts:
        messages.append({
            "role": "system",
            "content": "КОНТЕКСТ ПОТОЧНОГО КЛІЄНТА:\n" + "\n\n".join(context_parts)
        })

    content = []
    if user_text:
        content.append({"type": "text", "text": user_text})

    if image_bytes:
        base64_image = base64.b64encode(image_bytes).decode('utf-8')
        content.append({
            "type": "image_url",
            "image_url": {
                "url": f"data:image/jpeg;base64,{base64_image}"
            }
        })

    if not content:
        return "Будь ласка, напишіть ваше запитання або надішліть скріншот помилки."

    messages.append({
        "role": "user",
        "content": content
    })

    try:
        response = await client.chat.completions.create(
            model=OPENROUTER_MODEL,
            messages=messages,
            extra_headers={
                "HTTP-Referer": "https://github.com/shaaaaka/telegram-automation-bot",
                "X-Title": "Verification Support Bot"
            }
        )
        raw_response = response.choices[0].message.content.strip()
        
        # Додаємо обов'язковий підпис до кожного повідомлення
        signature = "\n\nЯ всього автоматизатор, якщо я не вирішую вашу проблему зверніться до менеджера"
        return f"{raw_response}{signature}"
    except Exception as e:
        logger.error(f"Помилка при запиті до OpenRouter: {e}")
        return "Виникла помилка при обробці запиту ШІ. Будь ласка, зачекайте на відповідь адміністратора."
